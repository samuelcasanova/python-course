import math

print("Basic math operations:")
print("Addition: 5 + 3 =", 5 + 3)
print("Subtraction: 10 - 4 =", 10 - 4)
print("Multiplication: 7 * 6 =", 7 * 6)
print("Division: 10 / 3 =", 10 / 3)
print("Integer Division: 10 // 3 =", 10 // 3)
print("Modulus: 10 % 3 =", 10 % 3)
print("Exponentiation: 2 ** 3 =", 2 ** 3)
print("Square root of 16:", 16 ** 0.5)

num = 1
num += 1
print("Incremented number:", num)

print("Using builtin math functions:")
print("Absolute value of -5:", abs(-5))
print("Rounded value of 3.7:", round(3.7))

print ("Using math module functions:")
print("Ceiling of 3.2:", math.ceil(3.2))
print("Floor of 3.8:", math.floor(3.8))
print("Square root of 25 using math.sqrt:", math.sqrt(25))
print("Value of Pi from math module:", math.pi)